

public class RelationalOperatorsDemo {

	public static void main(String[] args) {
		
		boolean b = 30 < 80;
		System.out.println("Value is " + b);
		
		b = 80 > 30;
		System.out.println("Value is " + b);
		
		b = (3 >= 3);
		System.out.println("Value is " + b);

		b = (5 <= 10);
		System.out.println("Value is " + b);

		b = (3 == 10);
		System.out.println("Value is " + b);

		b = (10 != 10);
		System.out.println("Value is " + b);
		
		
	}
}
