
public class ConditionsRepresentationSimple {

	public static void main(String[] args) {
		
		boolean someCondition = false;
		
		if (someCondition) {
			System.out.println("Value of condition ->" + someCondition);			
		}
				
		someCondition = true;
		
		if (someCondition) {
			System.out.println("Value of condition ->" + someCondition);					
		}
	}
}
