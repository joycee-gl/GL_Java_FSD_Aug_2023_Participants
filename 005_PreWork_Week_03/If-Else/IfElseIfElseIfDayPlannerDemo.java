
public class IfElseIfElseIfDayPlannerDemo {

	public static void main(String[] args) {
		
		String dayOfWeek = "Saturday";
		
		if (dayOfWeek.equals("Monday")) {
			System.out.println("Go to dance class");
		}else if (dayOfWeek.equals("Tuesday")) {
			System.out.println("Go to music class");
		}else if (dayOfWeek.equals("Wednesday")) {
			System.out.println("Go to Go to temple/mosque/church");
		}else if (dayOfWeek.equals("Thursday")) {
			System.out.println("Go to park");
		}else if (dayOfWeek.equals("Friday")) {
			System.out.println("Go to a movie");
		}else if (dayOfWeek.equals("Saturday")) {
			System.out.println("Go to meet friends");
		}else if (dayOfWeek.equals("Sunday")) {
			System.out.println("Relax and do nothing");
		}
	}
}
